export const dither = (
  frame: Uint8Array,
  width: number,
  height: number
): Uint8Array => {
  const len = width * height;
  const buf = new Int16Array(len);

  // Copy frame to buf
  for (let i = 0; i < len; i++) {
    buf[i] = frame[i];
  }

  for (let y = 0; y < height; y++) {
    for (let x = 0; x < width; x++) {
      const idx = y * width + x;
      const col = buf[idx] < 128 ? 0 : 255;
      const err = buf[idx] - col;
      buf[idx] = col;
      if (x + 1 < width) buf[idx + 1] += (err * 7) >> 4;
      if (y + 1 === height) continue;

      if (x > 0) buf[idx + width - 1] += (err * 3) >> 4;
      buf[idx + width] += (err * 5) >> 4;
      if (x + 1 < width) buf[idx + width + 1] += (err * 1) >> 4;
    }
  }

  // Copy buf back to frame
  for (let i = 0; i < len; i++) {
    frame[i] = buf[i];
  }

  return frame;
};

export const edges = (
  frame: Uint8Array,
  width: number,
  height: number
): Uint8Array => {
  const kernel: number[][] = [
    [-1, -1, -1],
    [-1, 9, -1],
    [-1, -1, -1],
  ];
  const copy = new Uint8Array(frame);
  const result = new Uint8Array(width * height);

  for (let x = 1; x < width - 1; x++) {
    for (let y = 1; y < height - 1; y++) {
      let sum = 0;
      for (let kx = -1; kx <= 1; kx++) {
        for (let ky = -1; ky <= 1; ky++) {
          const val = copy[x + kx + (y + ky) * width];
          sum += kernel[ky + 1][kx + 1] * val;
        }
      }
      sum = Math.max(0, Math.min(255, sum));
      result[x + y * width] = sum;
    }
  }

  // Copy the edges of the original image
  for (let x = 0; x < width; x++) {
    result[x] = frame[x]; // Top edge
    result[x + (height - 1) * width] = frame[x + (height - 1) * width]; // Bottom edge
  }
  for (let y = 0; y < height; y++) {
    result[y * width] = frame[y * width]; // Left edge
    result[width - 1 + y * width] = frame[width - 1 + y * width]; // Right edge
  }

  return result;
};

export const bilinearInterp = (
  input: Uint8Array,
  inputWidth: number,
  inputHeight: number,
  outputWidth: number,
  outputHeight: number
): Uint8Array => {
  const output = new Uint8Array(outputWidth * outputHeight);
  const xRatio = (inputWidth - 1) / (outputWidth - 1);
  const yRatio = (inputHeight - 1) / (outputHeight - 1);

  for (let i = 0; i < outputHeight; i++) {
    for (let j = 0; j < outputWidth; j++) {
      const xL = Math.floor(xRatio * j);
      const yL = Math.floor(yRatio * i);
      const xH = Math.ceil(xRatio * j);
      const yH = Math.ceil(yRatio * i);

      const xWei = xRatio * j - xL;
      const yWei = yRatio * i - yL;

      const a = input[yL * inputWidth + xL];
      const b = input[yL * inputWidth + xH];
      const c = input[yH * inputWidth + xL];
      const d = input[yH * inputWidth + xH];

      const val = Math.round(
        a * (1 - xWei) * (1 - yWei) +
          b * xWei * (1 - yWei) +
          c * yWei * (1 - xWei) +
          d * xWei * yWei
      );

      output[i * outputWidth + j] = val;
    }
  }

  return output;
};

export const blurImage = (
  frame: Uint8Array,
  width: number,
  height: number
): Uint8Array => {
  const kernel: number[][] = [
    [1 / 16, 2 / 16, 1 / 16],
    [2 / 16, 4 / 16, 2 / 16],
    [1 / 16, 2 / 16, 1 / 16],
  ];

  const copy = new Uint8Array(frame);
  const result = new Uint8Array(width * height);

  for (let y = 1; y < height - 1; y++) {
    for (let x = 1; x < width - 1; x++) {
      let sum = 0;
      for (let ky = -1; ky <= 1; ky++) {
        for (let kx = -1; kx <= 1; kx++) {
          sum += kernel[ky + 1][kx + 1] * copy[x + kx + (y + ky) * width];
        }
      }
      result[x + y * width] = Math.round(sum);
    }
  }

  // Copy the edges of the original image
  for (let x = 0; x < width; x++) {
    result[x] = frame[x]; // Top edge
    result[x + (height - 1) * width] = frame[x + (height - 1) * width]; // Bottom edge
  }
  for (let y = 0; y < height; y++) {
    result[y * width] = frame[y * width]; // Left edge
    result[width - 1 + y * width] = frame[width - 1 + y * width]; // Right edge
  }

  return result;
};

